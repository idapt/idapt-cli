func createTestUser() error {
	testUserEmail = fmt.Sprintf("test-daemon-%d@test.idapt.ai", time.Now().UnixNano())

	body, _ := json.Marshal(map[string]interface{}{
		"users": []map[string]string{
			{"email": testUserEmail, "password": "TestPassword123!@#", "name": "Daemon Test User"},
		},
	})

	req, _ := http.NewRequest("POST", appURL+"/api/admin/test/create-users", bytes.NewReader(body))
	req.Header.Set("Content-Type", "application/json")
	req.Header.Set("x-test-secret", testSecret)
	req.Header.Set("Origin", appURL)

	resp, err := http.DefaultClient.Do(req)
	if err != nil {
		return fmt.Errorf("create user: %w", err)
	}
	defer resp.Body.Close()

	if resp.StatusCode != http.StatusOK {
		respBody, _ := io.ReadAll(resp.Body)
		return fmt.Errorf("create user returned %d: %s", resp.StatusCode, string(respBody))
	}

	var rawResp map[string]interface{}
	respBody, _ := io.ReadAll(resp.Body)
	if err := json.Unmarshal(respBody, &rawResp); err != nil {
		return fmt.Errorf("parse create user response: %w (body: %s)", err, string(respBody))
	}

	users, ok := rawResp["users"].([]interface{})
	if !ok || len(users) == 0 {
		return fmt.Errorf("no users in response: %s", string(respBody))
	}
	user0, _ := users[0].(map[string]interface{})
	testUserActorID, _ = user0["actorId"].(string)
	sessionToken, _ := user0["sessionToken"].(string)
	if testUserActorID == "" {
		return fmt.Errorf("no actorId in response: %s", string(respBody))
	}

	// Build cookie header from session token
	if sessionToken != "" {
		testUserAuth = "better-auth.session_token=" + sessionToken
	} else {
		return fmt.Errorf("no sessionToken in response: %s", string(respBody))
	}

	// Get personal project
	req2, _ := http.NewRequest("GET", appURL+"/api/projects", nil)
	req2.Header.Set("Cookie", testUserAuth)
	req2.Header.Set("Origin", appURL)
	resp2, err := http.DefaultClient.Do(req2)
	if err != nil {
		return fmt.Errorf("get projects: %w", err)
	}
	defer resp2.Body.Close()

	var projectsResp struct {
		Projects []map[string]interface{} `json:"projects"`
	}
	json.NewDecoder(resp2.Body).Decode(&projectsResp)
	for _, p := range projectsResp.Projects {
		if p["isPersonal"] == true {
			testProjectID, _ = p["id"].(string)
			break
		}
	}
	if testProjectID == "" {
		return fmt.Errorf("no personal project found")
	}

	// Grant max subscription for machine features
	grantBody, _ := json.Marshal(map[string]string{
		"email": testUserEmail, "tier": "max",
	})
	req3, _ := http.NewRequest("POST", appURL+"/api/admin/test/grant-subscription", bytes.NewReader(grantBody))
	req3.Header.Set("Content-Type", "application/json")
	req3.Header.Set("x-test-secret", testSecret)
	req3.Header.Set("Origin", appURL)
	resp3, _ := http.DefaultClient.Do(req3)
	if resp3 != nil {
		resp3.Body.Close()
	}

	return nil
}

func createTestMachine() error {
	body, _ := json.Marshal(map[string]interface{}{
		"action":    "create-machine",
		"projectId": testProjectID,
		"actorId":   testUserActorID,
		"baseImage": "ubuntu-24.04",
	})

	req, _ := http.NewRequest("POST", appURL+"/api/admin/test/managed-machine-state", bytes.NewReader(body))
	req.Header.Set("Content-Type", "application/json")
	req.Header.Set("x-test-secret", testSecret)
	req.Header.Set("Origin", appURL)

	resp, err := http.DefaultClient.Do(req)
	if err != nil {
		return fmt.Errorf("create machine: %w", err)
	}
	defer resp.Body.Close()

	if resp.StatusCode != http.StatusOK && resp.StatusCode != http.StatusCreated {
		respBody, _ := io.ReadAll(resp.Body)
		return fmt.Errorf("create machine returned %d: %s", resp.StatusCode, string(respBody))
	}

	// Parse response to get auto-generated IDs
	var createResp struct {
		ManagedMachine struct {
			ID        string `json:"id"`
			MachineID string `json:"machineId"`
			Slug      string `json:"slug"`
		} `json:"managedMachine"`
	}
	respBody2, _ := io.ReadAll(resp.Body)
	if err := json.Unmarshal(respBody2, &createResp); err != nil {
		return fmt.Errorf("parse create machine response: %w (body: %s)", err, string(respBody2))
	}
	managedMachineID = createResp.ManagedMachine.ID
	appMachineID = createResp.ManagedMachine.MachineID
	machineSlug = createResp.ManagedMachine.Slug
	log.Printf("Machine created: managedMachineId=%s, machineId=%s, slug=%s", managedMachineID, appMachineID, machineSlug)

	// Force to running state
	stateBody, _ := json.Marshal(map[string]interface{}{
		"action":           "set-state",
		"managedMachineId": managedMachineID,
		"state":            "running",
	})

	req2, _ := http.NewRequest("POST", appURL+"/api/admin/test/managed-machine-state", bytes.NewReader(stateBody))
	req2.Header.Set("Content-Type", "application/json")
	req2.Header.Set("x-test-secret", testSecret)
	req2.Header.Set("Origin", appURL)

	resp2, err := http.DefaultClient.Do(req2)
	if err != nil {
		return fmt.Errorf("set state: %w", err)
	}
	resp2.Body.Close()

	return nil
}

func fetchHeartbeatSecret() error {
	req, _ := http.NewRequest("GET", fmt.Sprintf("%s/api/admin/test/machine-heartbeat-secret?machineId=%s", appURL, machineID), nil)
	req.Header.Set("x-test-secret", testSecret)
	req.Header.Set("Origin", appURL)

	resp, err := http.DefaultClient.Do(req)
	if err != nil {
		return err
	}
	defer resp.Body.Close()

	var result struct {
		Secret string `json:"secret"`
	}
	if err := json.NewDecoder(resp.Body).Decode(&result); err != nil {
		return err
	}
	machineToken = result.Secret
	return nil
}


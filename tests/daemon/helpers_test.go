// daemonExec runs a command inside the daemon container via /__test/exec.
func daemonExec(t *testing.T, command string) (string, string) {
	t.Helper()
	resp := daemonRequest(t, "POST", "/__test/exec",
		withJSON(map[string]string{"command": command}))
	result := readJSON(t, resp)
	output, _ := result["output"].(string)
	errStr, _ := result["error"].(string)
	return output, errStr
}


// Global test state — populated by TestMain.
var (
	daemonURL     string // e.g. https://daemon-test-api:8443
	daemonHTTPURL string // e.g. http://daemon-test-api:8080
	daemonPort9001 string // e.g. https://daemon-test-api:9001
	appURL        string // e.g. http://test-server-api:3000
	testSecret    string
	machineID     string
	machineToken  string // HMAC heartbeat secret (hex-encoded)

	managedMachineID string // auto-generated by create-machine
	appMachineID     string // the machine.id in the app DB (different from daemon's IDAPT_MACHINE_ID)
	machineSlug      string // auto-generated slug for the test machine

	// Test user credentials
	testUserEmail   string
	testUserAuth    string // cookie header
	testUserActorID string
	testProjectID   string

)

func TestMain(m *testing.M) {
	daemonURL = envOrFatal("DAEMON_TEST_URL")
	daemonHTTPURL = envOrFatal("DAEMON_HTTP_URL")
	daemonPort9001 = os.Getenv("DAEMON_PORT_9001_URL")
	if daemonPort9001 == "" {
		daemonPort9001 = strings.Replace(daemonURL, ":8443", ":9001", 1)
	}
	appURL = envOrFatal("APP_TEST_URL")
	testSecret = envOrFatal("TEST_SECRET")
	machineID = envOrFatal("DAEMON_MACHINE_ID")

	// Wait for daemon to be healthy
	if err := waitForDaemonHealthy(60 * time.Second); err != nil {
		log.Fatalf("Daemon not healthy: %v", err)
	}
	log.Println("Daemon is healthy")

	// Create test user via app
	if err := createTestUser(); err != nil {
		log.Fatalf("Failed to create test user: %v", err)
	}
	log.Printf("Test user created: %s (actor: %s)", testUserEmail, testUserActorID)

	// Create managed machine record in app (gets auto-generated IDs)
	if err := createTestMachine(); err != nil {
		log.Printf("WARN: Could not create test machine (some tests may skip): %v", err)
	} else {
		log.Printf("Test machine created: machineId=%s managedMachineId=%s slug=%s", appMachineID, managedMachineID, machineSlug)
		// Update daemon's JWT validator to accept the managedMachineId
		// (the auth endpoint uses managedMachineId as JWT "mid" claim)
		if err := setDaemonMachineID(managedMachineID); err != nil {
			log.Printf("WARN: Could not set daemon machine ID: %v", err)
		}
	}

	// Fetch heartbeat secret from app
	if err := fetchHeartbeatSecret(); err != nil {
		log.Printf("WARN: Could not fetch heartbeat secret: %v", err)
	} else {
		log.Println("Heartbeat secret fetched")
	}

	os.Exit(m.Run())
}

// --- HTTP helpers ---

// daemonClient returns an HTTP client that skips TLS verification and does NOT follow redirects.
func daemonClient() *http.Client {
	return &http.Client{
		Transport: &http.Transport{
			TLSClientConfig: &tls.Config{InsecureSkipVerify: true},
		},
		CheckRedirect: func(req *http.Request, via []*http.Request) error {
			return http.ErrUseLastResponse // don't follow redirects
		},
		Timeout: 30 * time.Second,
	}
}

// daemonClientWithCookies returns a client that handles cookies (for auth flow testing).
func daemonClientWithCookies() *http.Client {
	jar, _ := cookiejar.New(nil)
	return &http.Client{
		Transport: &http.Transport{
			TLSClientConfig: &tls.Config{InsecureSkipVerify: true},
		},
		Jar:     jar,
		Timeout: 30 * time.Second,
		CheckRedirect: func(req *http.Request, via []*http.Request) error {
			return http.ErrUseLastResponse
		},
	}
}

type requestOption func(*http.Request)

func withCookie(name, value string) requestOption {
	return func(r *http.Request) {
		r.AddCookie(&http.Cookie{Name: name, Value: value})
	}
}

func withHeader(key, value string) requestOption {
	return func(r *http.Request) {
		r.Header.Set(key, value)
	}
}

func withBearer(token string) requestOption {
	return withHeader("Authorization", "Bearer "+token)
}

func withJSON(body interface{}) requestOption {
	return func(r *http.Request) {
		data, _ := json.Marshal(body)
		r.Body = io.NopCloser(bytes.NewReader(data))
		r.ContentLength = int64(len(data))
		r.Header.Set("Content-Type", "application/json")
	}
}

// daemonRequest makes an HTTPS request to the daemon on the default port.
func daemonRequest(t *testing.T, method, path string, opts ...requestOption) *http.Response {
	t.Helper()
	return doRequest(t, daemonURL+path, method, opts...)
}

// daemonRequestPort makes an HTTPS request to the daemon on a specific port.
func daemonRequestPort(t *testing.T, baseURL, method, path string, opts ...requestOption) *http.Response {
	t.Helper()
	return doRequest(t, baseURL+path, method, opts...)
}

// daemonHTTPRequest makes an HTTP (not HTTPS) request to the daemon.
func daemonHTTPRequest(t *testing.T, method, path string, opts ...requestOption) *http.Response {
	t.Helper()
	return doRequest(t, daemonHTTPURL+path, method, opts...)
}

// appRequest makes a request to the app server.
func appRequest(t *testing.T, method, path string, opts ...requestOption) *http.Response {
	t.Helper()
	return doRequest(t, appURL+path, method, opts...)
}

func doRequest(t *testing.T, url, method string, opts ...requestOption) *http.Response {
	t.Helper()
	req, err := http.NewRequest(method, url, nil)
	if err != nil {
		t.Fatalf("Failed to create request: %v", err)
	}
	for _, opt := range opts {
		opt(req)
	}
	resp, err := daemonClient().Do(req)
	if err != nil {
		t.Fatalf("Request failed: %v", err)
	}
	return resp
}

func readBody(t *testing.T, resp *http.Response) string {
	t.Helper()
	defer resp.Body.Close()
	body, err := io.ReadAll(resp.Body)
	if err != nil {
		t.Fatalf("Failed to read body: %v", err)
	}
	return string(body)
}

func readJSON(t *testing.T, resp *http.Response) map[string]interface{} {
	t.Helper()
	body := readBody(t, resp)
	var result map[string]interface{}
	if err := json.Unmarshal([]byte(body), &result); err != nil {
		t.Fatalf("Failed to parse JSON: %v\nBody: %s", err, body)
	}
	return result
}

// --- HMAC helpers ---

// computeHMAC computes HMAC-SHA256 for daemon management APIs.
// Format: METHOD:PATH:TIMESTAMP
// The daemon hex-decodes the machineToken before using it as HMAC key,
// so we must do the same here (the app returns it as hex-encoded bytes).
func computeHMAC(method, path, timestamp, secret string) string {
	message := fmt.Sprintf("%s:%s:%s", method, path, timestamp)
	keyBytes, err := hex.DecodeString(secret)
	if err != nil {
		keyBytes = []byte(secret) // fallback: raw bytes if not valid hex
	}
	mac := hmac.New(sha256.New, keyBytes)
	mac.Write([]byte(message))
	return hex.EncodeToString(mac.Sum(nil))
}

func withHMAC(method, path, secret string) []requestOption {
	timestamp := strconv.FormatInt(time.Now().Unix(), 10)
	sig := computeHMAC(method, path, timestamp, secret)
	return []requestOption{
		withHeader("X-Machine-Signature", sig),
		withHeader("X-Machine-Timestamp", timestamp),
	}
}


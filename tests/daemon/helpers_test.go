// setDaemonMachineID updates the daemon's JWT validator to accept the given machineId.
func setDaemonMachineID(newMachineID string) error {
	body, _ := json.Marshal(map[string]string{"machineId": newMachineID})
	req, _ := http.NewRequest("POST", daemonURL+"/__test/set-machine-id", bytes.NewReader(body))
	req.Header.Set("Content-Type", "application/json")
	client := &http.Client{
		Transport: &http.Transport{
			TLSClientConfig: &tls.Config{InsecureSkipVerify: true},
		},
		Timeout: 10 * time.Second,
	}
	resp, err := client.Do(req)
	if err != nil {
		return err
	}
	resp.Body.Close()
	if resp.StatusCode != http.StatusOK {
		return fmt.Errorf("set-machine-id returned %d", resp.StatusCode)
	}
	return nil
}


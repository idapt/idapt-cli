// waitForDaemonHealthy polls the daemon health endpoint until success or timeout.
func waitForDaemonHealthy(timeout time.Duration) error {
	deadline := time.Now().Add(timeout)
	client := daemonClient()
	for time.Now().Before(deadline) {
		req, _ := http.NewRequest("GET", daemonURL+"/api/health", nil)
		resp, err := client.Do(req)
		if err == nil {
			resp.Body.Close()
			if resp.StatusCode == http.StatusOK {
				return nil
			}
		}
		time.Sleep(1 * time.Second)
	}
	return fmt.Errorf("daemon not healthy after %v", timeout)
}


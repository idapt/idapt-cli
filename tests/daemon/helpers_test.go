// issueJWTViaApp calls the app's auth endpoint and extracts the JWT from the redirect URL.
func issueJWTViaApp(t *testing.T, path string) string {
	t.Helper()

	if machineSlug == "" {
		t.Skip("No machine slug available — machine creation may have failed")
	}

	// Ensure daemon's JWT validator accepts this machine's managedMachineId
	// (may have been reset by a restart test — the auth endpoint uses managedMachineId as JWT "mid")
	if managedMachineID != "" {
		if err := setDaemonMachineID(managedMachineID); err != nil {
			t.Logf("WARN: Could not set daemon machine ID: %v", err)
		}
	}

	// Call the app's machine auth endpoint with session cookie
	req, err := http.NewRequest("GET", fmt.Sprintf("%s/api/managed-machines/auth?slug=%s&path=%s", appURL, machineSlug, path), nil)
	if err != nil {
		t.Fatalf("Failed to create auth request: %v", err)
	}
	req.Header.Set("Cookie", testUserAuth)
	req.Header.Set("Origin", appURL)

	client := &http.Client{
		CheckRedirect: func(req *http.Request, via []*http.Request) error {
			return http.ErrUseLastResponse
		},
		Timeout: 10 * time.Second,
	}
	resp, err := client.Do(req)
	if err != nil {
		t.Fatalf("Auth request failed: %v", err)
	}
	defer resp.Body.Close()

	if resp.StatusCode != http.StatusFound && resp.StatusCode != http.StatusTemporaryRedirect {
		body, _ := io.ReadAll(resp.Body)
		t.Fatalf("Expected redirect from auth endpoint, got %d: %s", resp.StatusCode, string(body))
	}

	// Extract token from redirect URL: https://{slug}.localhost:PORT/__auth_callback?token=JWT&path=/...
	location := resp.Header.Get("Location")
	if location == "" {
		t.Fatal("No Location header in auth redirect")
	}
	t.Logf("Auth redirect Location: %s", location[:min(len(location), 120)])

	// Parse token from URL
	parts := strings.Split(location, "token=")
	if len(parts) < 2 {
		t.Fatalf("No token in redirect URL: %s", location)
	}
	token := parts[1]
	if idx := strings.Index(token, "&"); idx != -1 {
		token = token[:idx]
	}

	t.Logf("Extracted JWT (first 50 chars): %s...", token[:min(len(token), 50)])
	return token
}

// --- SSH helpers ---

func sshExec(t *testing.T, command string) (string, error) {
	t.Helper()
	host := os.Getenv("SSH_TEST_HOST")
	port := os.Getenv("SSH_TEST_PORT")
	user := os.Getenv("SSH_TEST_USER")
	password := os.Getenv("SSH_TEST_PASSWORD")

	if host == "" || port == "" {
		t.Skip("SSH_TEST_HOST/SSH_TEST_PORT not set, skipping SSH test")
	}

	config := &ssh.ClientConfig{
		User: user,
		Auth: []ssh.AuthMethod{
			ssh.Password(password),
		},
		HostKeyCallback: ssh.InsecureIgnoreHostKey(),
		Timeout:         10 * time.Second,
	}

	client, err := ssh.Dial("tcp", host+":"+port, config)
	if err != nil {
		return "", fmt.Errorf("SSH dial: %w", err)
	}
	defer client.Close()

	session, err := client.NewSession()
	if err != nil {
		return "", fmt.Errorf("SSH session: %w", err)
	}
	defer session.Close()

	out, err := session.CombinedOutput(command)
	return string(out), err
}

